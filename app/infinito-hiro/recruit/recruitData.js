  const baseUrl = "https://infinito.yamadafooddev.com";

  const jobPosting = {
    "@context": "https://schema.org/",
    "@type": "JobPosting",
    "title": jobData.jobTitle,
    "description": jobData.jobDescription,
    "datePosted": "2024-05-01",
    "validThrough": "2025-12-31",
    "employmentType": jobData.employmentType === "正社員" || jobData.employmentType === "Full-time"
      ? "FULL_TIME"
      : ["FULL_TIME", "PART_TIME"],
    "hiringOrganization": {
      "@type": "Organization",
      "name": company.companyName,
      "sameAs": baseUrl,
      "logo": baseUrl + "/logo.png"
    },
    "jobLocation": {
      "@type": "Place",
      "address": {
        "@type": "PostalAddress",
        "streetAddress": lang === "ja" ? "赤坂5-1-38 赤坂東商ビル3階" : "5-1-38 Akasaka, Akasaka Tosho Bldg. 3F",
        "addressLocality": lang === "ja" ? "港区" : "Minato-ku",
        "addressRegion": lang === "ja" ? "東京都" : "Tokyo",
        "postalCode": "107-0052",
        "addressCountry": "JP"
      }
    },
    "baseSalary": {
      "@type": "MonetaryAmount",
      "currency": "JPY",
      "value": {
        "@type": "QuantitativeValue",
        "minValue": jobData.salaryMin || jobData.hourlyMin,
        "maxValue": jobData.salaryMax || jobData.hourlyMax,
        "unitText": jobData.hourlyMin ? "HOUR" : "MONTH"
      }
    },
    "url": baseUrl + job.path,
    "applicationContact": {
      "@type": "ContactPoint",
      "email": company.email,
      "telephone": company.phone
    }
  };

  return JSON.stringify(jobPosting, null, 2);
